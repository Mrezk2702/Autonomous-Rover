u8 TIMER1_u8SetCompareMatchValue(u8 Copy_u8Channel,u16 Copy_CTCValue)
{

	u8  Local_ErrorState=0;


	if(Copy_CTCValue<=65536&&Copy_u8Channel<=CHANNELC)
	{

		if(Copy_u8Channel==CHANNELA)
		{
			OCR1AH=(u8)(Copy_CTCValue>>8);
			OCR1AL=(u8)(Copy_CTCValue);


		}
		else if (Copy_u8Channel==CHANNELB)
		{
			OCR1BH=(u8)(Copy_CTCValue>>8);
			OCR1BL=(u8)(Copy_CTCValue);


		}
		else if (Copy_u8Channel==CHANNELC)
		{

			OCR1CH=(u8)(Copy_CTCValue>>8);
			OCR1CL=(u8)(Copy_CTCValue);
		}

	}

	else
	{
		Local_ErrorState=1;
	}

	return Local_ErrorState;
}

u8 TIMER1_u8SetICRValue(u16 Copy_ICR1Value)
{

	u8  Local_ErrorState=0;


	if(Copy_ICR1Value<=65536)
	{
		ICR1H=(u8)(Copy_ICR1Value>>8);
		ICR1L=(u8)(Copy_ICR1Value);
	}


	else
	{
		Local_ErrorState=1;
	}

	return Local_ErrorState;
}


