/***********************************************Timer0****************************************************************************/

void Timer0_voidInit(void)
{
	/*Normal Timer operation*/
#if TIMER0_MODE==NORMAL
	CLEAR_BIT(TCCR0,TCCR0_WGM00);
	CLEAR_BIT(TCCR0,TCCR0_WGM01);


#elif TIMER0_MODE==PHASE_CORRECT_PWM
	SET_BIT(TCCR0,TCCR0_WGM00);
	CLEAR_BIT(TCCR0,TCCR0_WGM01);

#if  TIMER0_WAVE_FORM == COCWUSOCWD
	CLEAR_BIT(TCCR0,TCCR0_COM00);
	SET_BIT(TCCR0,TCCR0_COM01);

#elif TIMER0_WAVE_FORM == SOCWUCOCWD
	SET_BIT(TCCR0,TCCR0_COM00);
	SET_BIT(TCCR0,TCCR0_COM01);
#endif



#elif TIMER0_MODE==CTC
	CLEAR_BIT(TCCR0,TCCR0_WGM00);
	SET_BIT(TCCR0,TCCR0_WGM01);


#if TIMER0_WAVE_FORM == DISCONNECT
	CLEAR_BIT(TCCR0,TCCR0_COM00);
	CLEAR_BIT(TCCR0,TCCR0_COM01);
#elif TIMER0_WAVE_FORM == TOGGLE
	SET_BIT(TCCR0,TCCR0_COM00);
	CLEAR_BIT(TCCR0,TCCR0_COM01);
#elif TIMER0_WAVE_FORM == CLEAR
	CLEAR_BIT(TCCR0,TCCR0_COM00);
	SET_BIT(TCCR0,TCCR0_COM01);
#elif TIMER0_WAVE_FORM == SET
	SET_BIT(TCCR0,TCCR0_COM00);
	SET_BIT(TCCR0,TCCR0_COM01);

#endif


#elif TIMER0_MODE==FAST_PWM
	SET_BIT(TCCR0,TCCR0_WGM00);
	SET_BIT(TCCR0,TCCR0_WGM01);

#if  TIMER0_WAVE_FORM == COCSOT
	CLEAR_BIT(TCCR0,TCCR0_COM00);
	SET_BIT(TCCR0,TCCR0_COM01);

#elif TIMER0_WAVE_FORM == SOCCOT
	SET_BIT(TCCR0,TCCR0_COM00);
	SET_BIT(TCCR0,TCCR0_COM01);
#endif

#endif


#if TIMER0_PRE_SCALER == NO_PRESCALER
	SET_BIT(TCCR0,TCCR0_CS00);
	CLEAR_BIT(TCCR0,TCCR0_CS01);
	CLEAR_BIT(TCCR0,TCCR0_CS02);
#elif TIMER0_PRE_SCALER == TIMERS_PRESCALER_BY_8
	CLEAR_BIT(TCCR0,TCCR0_CS00);
	SET_BIT(TCCR0,TCCR0_CS01);
	CLEAR_BIT(TCCR0,TCCR0_CS02);
#elif TIMER0_PRE_SCALER == TIMERS_PRESCALER_BY_32
	SET_BIT(TCCR0,TCCR0_CS00);
	SET_BIT(TCCR0,TCCR0_CS01);
	CLEAR_BIT(TCCR0,TCCR0_CS02);
#elif TIMER0_PRE_SCALER == TIMERS_PRESCALER_BY_64
	CLEAR_BIT(TCCR0,TCCR0_CS00);
	CLEAR_BIT(TCCR0,TCCR0_CS01);
	SET_BIT(TCCR0,TCCR0_CS02);
#elif TIMER0_PRE_SCALER == TIMERS_PRESCALER_BY_128
	SET_BIT(TCCR0,TCCR0_CS00);
	CLEAR_BIT(TCCR0,TCCR0_CS01);
	SET_BIT(TCCR0,TCCR0_CS02);
#elif TIMER0_PRE_SCALER == TIMERS_PRESCALER_BY_256
	CLEAR_BIT(TCCR0,TCCR0_CS00);
	SET_BIT(TCCR0,TCCR0_CS01);
	SET_BIT(TCCR0,TCCR0_CS02);
#elif TIMER0_PRE_SCALER == TIMERS_PRESCALER_BY_1024
	SET_BIT(TCCR0,TCCR0_CS00);
	SET_BIT(TCCR0,TCCR0_CS01);
	SET_BIT(TCCR0,TCCR0_CS02);

#elif TIMER0_PRE_SCALER == TIMERS_EXTERNAL_CLOCK

	SET_BIT(ASSR,ASSR_AS0);
#endif


#if TIMER0_OVF_INTERRUPT == ENABLE
	SET_BIT(TIMSK,TIMSK_TOIE0);

#elif TIMER0_OVF_INTERRUPT == DISABLE

	CLEAR_BIT(TIMSK,TIMSK_TOIE0);
#endif

#if TIMER0_CTC_INTERRUPT == ENABLE
	SET_BIT(TIMSK,TIMSK_OCIE0);

#elif TIMER0_CTC_INTERRUPT == DISABLE

	CLEAR_BIT(TIMSK,TIMSK_OCIE0);
#endif

}

